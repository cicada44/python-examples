# Trivial example for cycle 'while' in Python3
#
#-----------------------------------------------------------------------------

n = int(input("Input n: "))

while n > 0:
    print("n is", n)
    n = n - 1

# Lets do is forever and count iteration!!!
n = 0
while True:
    print("Where's the end???")
    print("Iteration ", n)
    n = n + 1
