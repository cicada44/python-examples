# Trivial example for variables in Python3
#
#-----------------------------------------------------------------------------

# Let's call it 'x' and print it
x = 5
print("x -", x, end='\n\n')

# Show it your school math teacher!
x = x + 5
print("x -", x, end='\n\n')

# And something more...

y = x + 1
print("y -", y, end='\n\n')

xplusy = x + y
print("xplusy -", xplusy, end='\n\n')

qwerty = "qwerty"
print("qwerty -", qwerty, end='\n\n')

# 'Concatenation'
verylongvariable = "very" + "long" + "variable"
print("verylongvariable -", verylongvariable)

